### Import packages ###

import sys, os
import argparse as argp
import scanpy as sc
import pandas as pd
import glob
import numpy as np

### Input variables ###
parser = argp.ArgumentParser(description = 'For each cellBC in the adata object get the consensus sequence of the linBC using starcode')
parser.add_argument('--CSVdir', help = 'directory to .linBCs.csv files generated by extract_BCs.v2fast.py')
parser.add_argument('--h5ad', help = 'processed h5ad object')
parser.add_argument('--distance', help = 'Max allowable Levensthein distance used by starcode', type= int , default=3)
parser.add_argument('--outdir', help = 'output directory', type = str, default = 'starcode/')
args = parser.parse_args()

CSVdir = args.CSVdir
h5ad = args.h5ad
starcode_distance = args.distance
outdir = args.outdir

### Parameters ###

threads = 4

### Functions ###

def hamming_distance(BC_seq):
    '''Generate all possible barcode sequences with 1 hamming distance difference'''
    nucleotides = ['A', 'C', 'G', 'T']
    neighbors = [BC_seq]

    for i, n in enumerate(BC_seq):
        for alt in nucleotides:
            if alt != n and alt != 'N':
                neighbor = BC_seq[:i] + alt + BC_seq[i+1:]
                neighbors.append(neighbor)
    return neighbors

def starcode(barcodes, cellbc, BC_structure, outdir='./', starcode_distance=starcode_distance):
	os.system(f'mkdir -p {outdir}')
	cellbc = outdir + cellbc
	outfile = f'{cellbc}.{BC_structure}.linBCs.txt'
	with open(outfile, 'a') as fout:
		for bc in barcodes:
			fout.write(f'{bc}\n')
	fout.close()
	os.system(f'starcode -i {outfile} -o {cellbc}.{BC_structure}.clustered.txt -d {starcode_distance} --print-clusters -t {threads}')


### Code ###

if not outdir.endswith('/'):
	outdir = outdir+'/'

os.system(f'mkdir -p {outdir}')

adata = sc.read_h5ad(h5ad)

cell_barcodes = list(set([cellbc.split('-')[0] for cellbc in adata.obs.index]))

df_bc = pd.concat([pd.read_csv(file) for file in glob.glob(f'{CSVdir}/*.linBCs.csv')])

df_bc = df_bc.loc[:,['cellBC','linBC','BC_structure']]
df_bc.index = range(df_bc.shape[0])
df_bc['cellBC_16'] = df_bc['cellBC'].apply(lambda x: x[:16])
df_bc['UMI_12'] = df_bc['cellBC'].apply(lambda x: x[16:16+12])

#df_bc = df_bc[df_bc['cellBC_16'].isin(cell_barcodes)]
df_bc1 = df_bc[df_bc['BC_structure'] == 'BC1']
df_bc2 = df_bc[df_bc['BC_structure'] == 'BC2']

for cellbc in cell_barcodes:
	neighbors = hamming_distance(cellbc)
	dfi = df_bc1[df_bc1['cellBC_16'].isin(neighbors)]
	nreads = dfi.shape[0]
	BC_sequences = list(dfi['linBC'])
	if nreads > 2:
		starcode(BC_sequences, cellbc, 'BC1', outdir=outdir)

for cellbc in cell_barcodes:
	neighbors = hamming_distance(cellbc)
	dfi = df_bc2[df_bc2['cellBC_16'].isin(neighbors)]
	nreads = dfi.shape[0]
	BC_sequences = list(dfi['linBC'])
	if nreads > 2:
		starcode(BC_sequences, cellbc, 'BC2', outdir=outdir)

